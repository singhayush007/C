/*
Which of the following is used to appropriately read a multi-word string.
1- gets()
2- puts()
3- printf()
4- scanf()
*/

// Answer : gets()

/*Reason : Use gets() for reading multi-word strings, but note that it is deprecated and unsafe due to the risk of buffer overflow.
Use puts() and printf() for output purposes only.
Use scanf() for reading input, but it has limitations with multi-word strings unless used with specific format specifiers or alternatives like fgets() for safer input handling.
*/